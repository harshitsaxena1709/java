public class calculator{
    
    public int add(int op1,int op2)
    {
        return op1+op2;
    }
    public int sub(int op1,int op2)
    {
        return op1-op2;
    } public int pro (int op1,int op2)
    {
        return op1*op2;
    } public int div(int op1,int op2)
    {
        return op1/op2;
    }
    public int mod(int op1,int op2)
    {
        return op1%op2;
    }
}